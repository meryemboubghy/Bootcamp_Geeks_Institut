month=(int(input("Entrer un mois :")))
if month >=3 and month <=5 :
   print("Springse")
elif month >=6 and month <=8 :
   print("Summerse")
elif month >=9 and month <=11 :
    print("Autumnse")
elif month == 12 or month == 1 or month == 2:
    print("Winterse")
else:
    print("Mois invalide")
 
 
